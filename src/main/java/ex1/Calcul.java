package ex1;

/**
 * Classe qui réalise des opérations arithmétiques
 * @author DIGINAMIC
 */
public class Calcul {

	/** Méthode qui retourne l'addition des paramètres a et b 
	 * 
	 * @param a Premier entier
	 * @param b Deuxième entier
	 * @return int L'addition des deux entiers
	 */
	public int additionneDeuxNombres(int a, int b){
		return a+b;
	}
	
	/**
	 * Méthode qui retourne la soustraction des paramètres a et b
	 * @param a Premier entier
	 * @param b Deuxième entier
	 * @return int La différence entre le premier et le deuxième entier
	 */
	public int soustraitDeuxNombres(int a, int b){
		return a-b;
	}
}
